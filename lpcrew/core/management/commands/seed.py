class Command(BaseCommand):
    help = "Создаёт стартовый контент (лендинг, заплывы, новости, настройки)."

    def add_arguments(self, parser):
        parser.add_argument("--force", action="store_true")

    def handle(self, *args, **options):
        if HomePage.objects.exists() and not options["force"]:
            self.stdout.write("Контент уже есть — пропускаю (--force для пересоздания).")
            return

        self._img_cache = {}
        root = Page.objects.get(depth=1)
        site = Site.objects.filter(is_default_site=True).first()
        old_home = site.root_page if site else None

        if old_home and old_home.id != root.id:
            if site:
                site.root_page = root
                site.save()
            old_home.delete()
            root.refresh_from_db()

        home = HomePage(title="LP Crew", slug="home")
        home.body = self._home_body()
        root.add_child(instance=home)
        if site:
            site.root_page = home
            site.save()

        self._seed_swims(home)
        self._seed_news(home)
        self._seed_settings()
        self.stdout.write(self.style.SUCCESS("Стартовый контент (14) создан."))

    # ---------- images ----------
    def img(self, fname, title):
        if title in self._img_cache:
            return self._img_cache[title]
        existing = Image.objects.filter(title=title).first()
        if existing:
            self._img_cache[title] = existing
            return existing
        path = settings.BASE_DIR / "seed" / "img" / fname
        if not path.exists():
            self.stdout.write(self.style.WARNING(f"нет файла {path}"))
            return None
        with PILImage.open(path) as im:
            w, h = im.size
        image = Image(title=title, width=w, height=h)
        with open(path, "rb") as f:
            image.file.save(fname, File(f), save=False)
        image.save()
        self._img_cache[title] = image
        return image

